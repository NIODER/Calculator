namespace Domain;

public static class Calculator
{
    public static decimal Add(decimal n1, decimal n2)
    {
        return n1 + n2;
    }

    public static decimal Subtract(decimal diminutive, decimal deductible)
    {
        return diminutive - deductible;
    }

    public static decimal Multiply(decimal n1, decimal n2)
    {
        return n1 * n2;
    }

    public static decimal Divide(decimal divisible, decimal divider)
    {
        return divisible / divider;
    }
}
